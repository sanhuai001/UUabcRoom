package com.uuabc.classroomlib.model;

public class SRSwitchModel {
    /**
     * type : 1
     * state : 2
     * extend : null
     */

    private int type;
    private int state;
    private String extend;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }

    public String getExtend() {
        return extend;
    }

    public void setExtend(String extend) {
        this.extend = extend;
    }
}
